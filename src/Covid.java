
    public class Covid {

        private double salario;
        private String nome;
        private int idade;

        public Covid(String nome, int idade) {
            this.nome = nome;
            this.idade = idade;
            salario = 800;
        }

        public double getSalario() {
            return salario;
        }

        public String getNome() {
            return nome;
        }

        public int getIdade() {
            return idade;
        }
    }




